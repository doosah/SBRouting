/**
 * Get all regions
 */
export const getRegions = () => {
  return Object.keys(warehouses);
};

/**
 * Get warehouse types for a region
 */
export const getWarehouseTypes = (region) => {
  return warehouses[region] ? Object.keys(warehouses[region]) : [];
};

/**
 * Get warehouses for a region and type
 */
export const getWarehouses = (region, type) => {
  return warehouses[region]?.[type] || [];
};

/**
 * Get all warehouses count
 */
export const getTotalWarehousesCount = () => {
  let count = 0;
  Object.values(warehouses).forEach((region) => {
    Object.values(region).forEach((typeWarehouses) => {
      count += typeWarehouses.length;
    });
  });
  return count;
};
